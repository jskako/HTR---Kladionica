    private void b_PotvrdaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_PotvrdaActionPerformed
        // TODO add your handling code here:
        //Hvatamo zadnji ID

        RS = CALIzb.main(Conn, "SELECT TOP 1 F04UIID FROM User_UI_Odobrenje ORDER BY F04UIID DESC");
        try {
            while (RS.next()) {
                myNextID = RS.getInt("F04UIID");
                myNextID += 1;
            }
            if (myNextID == 0) {
                myNextID += 1;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        //Provjera i upis
        try {
            if (sldr_UI.getValue() == 0 || sldr_UI.getValue() == 100) {
                if (!txt_UserID.getText().trim().equals("")) {
                    if (!txt_Iznos.getText().trim().equals("")) {
                        if (sldr_UI.getValue() == 0) {
                            double myEntVal = Double.parseDouble(txt_Iznos.getText().trim());
                            if (myEntVal <= UserBalance) {
                                RS = CALIzb.main(Conn, "insert into User_UI_Odobrenje values ('" + myNextID + "', '" + txt_Iznos.getText().trim() + "', '0', '" + txt_Opis.getText().trim() + "', GETDATE(), '" + userUsername + "', 'Split', '" + myUserID + "','0')");
                                dispose();
                            } else {
                                myMsg.setText("Nemate dovoljno na racunu!");
                                Timer timer = new Timer(2000, e -> myMsg.setText(""));
                                timer.setRepeats(false);
                                timer.start();
                            }
                        } else if (sldr_UI.getValue() == 100) {
                            RS = CALIzb.main(Conn, "insert into User_UI_Odobrenje values ('" + myNextID + "', '" + txt_Iznos.getText().trim() + "', '1', '" + txt_Opis.getText().trim() + "', GETDATE(), ' ', ' ', '" + myUserID + "','0')");
                            dispose();
                        }
                    }
                }
            }
        } catch (Exception f) {
            f.printStackTrace();
        }
    }//GEN-LAST:event_b_PotvrdaActionPerformed


